x = 10
y = 3

print(x + y)  # 덧셈: 13
print(x - y)  # 뺄셈: 7
print(x * y)  # 곱셈: 30
print(x / y)  # 나눗셈: 3.3333...
print(x // y)  # 몫: 3
print(x % y)  # 나머지: 1
print(x ** y)  # 거듭제곱: 1000

print(x == y)  # 같음: False
print(x != y)  # 같지 않음: True
print(x > y)  # 큼: True
print(x < y)  # 작음: False
print(x >= y)  # 크거나 같음: True
print(x <= y)  # 작거나 같음: False